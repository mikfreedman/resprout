package io.pivotal.model.soloist;

public class Sprout {

    private Object git;
    private Object terminal;
    private Object settings;
    private Object dock_preferences;
    private Homebrew homebrew;

    public Object getGit() {
        return git;
    }

    public void setGit(Object git) {
        this.git = git;
    }

    public Object getTerminal() {
        return terminal;
    }

    public void setTerminal(Object terminal) {
        this.terminal = terminal;
    }

    public Object getSettings() {
        return settings;
    }

    public void setSettings(Object settings) {
        this.settings = settings;
    }

    public Object getDock_preferences() {
        return dock_preferences;
    }

    public void setDock_preferences(Object dock_preferences) {
        this.dock_preferences = dock_preferences;
    }

    public Homebrew getHomebrew() {
        return homebrew;
    }

    public void setHomebrew(Homebrew homebrew) {
        this.homebrew = homebrew;
    }
}
